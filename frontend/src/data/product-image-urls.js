const FALLBACK_IMAGE_POOL = Object.values(CATEGORY_IMAGE_URLS).flat();

function normalizeCategory(value = '') {
  const txt = String(value || '')
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .trim();

  if (txt.includes('lacteo')) return 'lacteos';
  if (txt.includes('abarrote')) return 'abarrotes';
  if (txt.includes('enlatad')) return 'enlatados';
  if (txt.includes('bebida')) return 'bebidas';
  if (txt.includes('snack')) return 'snacks';
  if (txt.includes('higien')) return 'higiene';
  if (txt.includes('limpieza')) return 'limpieza';
  if (txt.includes('congelad')) return 'congelados';
  return txt;
}

export function getProductImageUrl(producto) {
  if (!producto) return '';

  // Prioridad 1: URL enviada por backend (si luego la agregas en BD/API)
  const fromApi = String(producto.imagen_url || '').trim();
  if (fromApi) return fromApi;

  // Prioridad 2: Mapeo local por id_producto
  const key = String(producto.id_producto ?? '');
  const explicit = PRODUCT_IMAGE_URLS[key] || PRODUCT_IMAGE_URLS[Number(key)];
  if (explicit) return explicit;

  // Prioridad 3: Mapeo automático por categoría.
  const categoryKey = normalizeCategory(producto.categoria);
  const list = CATEGORY_IMAGE_URLS[categoryKey];
  const pool = list && list.length > 0 ? list : FALLBACK_IMAGE_POOL;
  if (!pool || pool.length === 0) return '';

  const numericId = Number(key);
  const index = Number.isFinite(numericId) ? Math.abs(numericId) % pool.length : 0;
  return pool[index] || '';
}
